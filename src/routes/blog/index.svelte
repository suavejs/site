<script context="module">
	export function preload({ params, query }) {
		return this.fetch(`blog.json`).then(r => r.json()).then(posts => {
			return { posts };
		});
	}
</script>

<script>
	import Content from '../../components/Content.svelte';
	export let posts;
</script>
<svelte:head>
	<title>Blog</title>
</svelte:head>

<Content>
<h1>Recent posts</h1>

<ul>
	{#each posts as post}
		<li class="list-reset"><a rel='prefetch' href='blog/{post.slug}'>{post.title}</a></li>
	{/each}
</ul>

</Content>